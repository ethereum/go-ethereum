func DumpWitnessToFile(w *Witness) {
	enc := w.EncodeRLP()
	path, err := os.Getwd()
	if err != nil {
		panic("shite")
	}
	err = os.MkdirAll(fmt.Sprintf("%s/block-dump", path), 0755)
	if err != nil {
		panic("shite2")
	}
	outputFName := fmt.Sprintf("%d-%x.rlp", w.Block.NumberU64(), w.Block.Hash())
	err = os.WriteFile(path+"/block-dump/"+outputFName, enc, 0644)
	if err != nil {
		panic("shite 3")
	}
}
